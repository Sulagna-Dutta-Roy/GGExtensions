    public static void draw(Graphics2D g2, int x, int y, Color c, int l, int fl) {
        boolean b = false;
        if (l > fl * 0.6f) {
            return;
        } else {
            b = l > fl * 0.3f;
        }
        BufferedImage img = new BufferedImage(Block.SIZE, Block.SIZE, BufferedImage.TYPE_INT_ARGB);
        int rgb = c.darker().getRGB();
        for (int j = 0; j < Block.SIZE; j++) {
            for (int k = 0; k < Block.SIZE; k++) {
                if (b) {
                    if (LOW[k + j * Block.SIZE] == 1) {
                        img.setRGB(j, k, rgb);
                    }
                } else {
                    if (HIGHT[k + j * Block.SIZE] == 1) {
                        img.setRGB(j, k, rgb);
                    }
                }
            }
        }
        g2.drawImage(img, x, y, null);
    }

